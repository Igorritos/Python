# PROBLEMA: Calcular a media de dois números

# - obter o primeiro número (ed)
n1 = input("Digite o Primeiro número:")
n1 = float(n1)
# - obter o segundo número (ed)
n2 = float(input("Digite o Segundo número:"))

# - Calcular a média (pd)
med = n1 + n2 / 2
# - Exibir a média (sd)
print("A média de", n1, " com", n2, " é",med)
